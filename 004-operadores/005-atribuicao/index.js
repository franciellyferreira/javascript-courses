/*
    Variáveis:
        var = não é mais usada
        let = trabalha com escopo
        const = não se altera no meio do caminho
    Atribuição:
        = 
        +=
        -=
        *=
*/

let a = 1;
let b = 2;
console.log(a);
console.log(a + b);

a += 1;
console.log(a);

a = a + 1;
console.log(a);

a -= 1;
console.log(a);

a *= 2;
console.log(a);

a /= 2
console.log(a);
